export function unAccentise(word: string){
    word = word.toLowerCase();
    for(let i=0;i< word.length;++i){
        let char = word.charAt(i);
        if(latin_map.some(element => element.accent == char)){
            let replacement = latin_map.find((element:AccentCharacters) => element.accent == char)?.english;
            word = word.replace(char,replacement!);
        }
    }
    return word;
}